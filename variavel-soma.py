#variaveis dada no exercicio
INDICE = 13
SOMA = 0
K = 0

#enquanto k for menor que o incice(13)
while K < INDICE:
    #o k vai aumentando o valor até er maior que 13 (INDICE)
    K += 1
    #ENQUANTO O K AUMENTA, CADA NUMERO ALTERADO É ACRESENTADO EM "SOMA"
    SOMA += K
#IMPRIMIR A RESPOSTA DE SOMA
print(f"Valor de SOMA: {SOMA}")